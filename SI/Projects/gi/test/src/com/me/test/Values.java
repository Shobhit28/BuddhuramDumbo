package com.me.test;

public class Values {
	private int id;
	private int x;
	private int y;
	private int width;
	private int height;
	private int xoffset;
	private int yoffset;
	private int xadvance;
	public Values()
	{
		id=x=y=width=height=xoffset=yoffset=xadvance=0;
	}
	public int getXoffset() {
		return xoffset;
	}
	public void setXoffset(int xoffset) {
		this.xoffset = xoffset;
	}
	public int getYoffset() {
		return yoffset;
	}
	public void setYoffset(int yoffset) {
		this.yoffset = yoffset;
	}
	public int getXadvance() {
		return xadvance;
	}
	public void setXadvance(int xadvance) {
		this.xadvance = xadvance;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}

}
